/**
 * Trova l'indice di un elemento all'interno dell'array.
 * Restituisce -1 se l'elemento non è presente nell'array.
 * @param {Array} array - L'array in cui cercare l'elemento.
 * @param {*} element - L'elemento da cercare nell'array.
 * @returns {number} - L'indice dell'elemento nell'array, o -1 se l'elemento non è presente.
 */
function trovaIndice(array, element) {
    return array.indexOf(element);
}
